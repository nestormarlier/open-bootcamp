altura = input("Ingrese el alto del triangulo: ")
base = input("Ingrese base del triangulo: ")

print("El área del triangulo es " + str(float(base)*float(altura)/2))  # Area = (base * altura)/2

radio = input("Ingrese valor de radio del círculo: ")

area = float(radio)*float(radio)*3.14 # A = π r²
print("El área del círculo es "+ str(area))